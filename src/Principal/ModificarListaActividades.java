    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        String NombreLista = txtNombreListaTareas.getText();
        if (!"".equals(NombreTablero)) {
            ArrayList<ActividadesLista> ListaActividad = DevolverArrayListaActividades(CodigoArchivo);
            ActividadesLista ModificarListadoActividades = ListaActividad.stream().filter(t -> t.getCodigoListaAc().equals(this.CodigoLista)).findFirst().get();

            ModificarListadoActividades.setCodigoTarea(this.CodigoArchivo.trim());
            ModificarListadoActividades.setCodigoListaAc(this.CodigoLista.trim());
            ModificarListadoActividades.setNombreListaA(NombreLista.trim());

            ModificarArchivoTxtListaA(ListaActividad, this.CodigoArchivo);
            lblmensaje.setForeground(Color.green);
            lblmensaje.setText("Listado modificado con exito.");
            lblmensaje.setVisible(true);

        }
    }//GEN-LAST:event_btnModificarActionPerformed

