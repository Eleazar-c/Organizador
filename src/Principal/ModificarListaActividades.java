    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
      int valor = 1;
        File Archivo = new File("./src/resource/Actividades/" + CodigoLista + ".txt");
        if (Archivo.isFile()) {
            if (Archivo.delete()) {
                valor = 1;
            } else {
                valor = 0;
            }
        }

        if (valor == 1) {
            ArrayList<ActividadesLista> ListaActividad = DevolverArrayListaActividades(CodigoArchivo);
            ListaActividad.removeIf(t -> t.getCodigoListaAc().equals(CodigoLista));

            ModificarArchivoTxtListaA(ListaActividad, CodigoArchivo);
            lblmensaje.setVisible(true);
            lblmensaje.setForeground(Color.green);
            lblmensaje.setText("Lista de actividades eliminadas con exito.");
            btnModificar.setEnabled(false);
            btnEliminar.setEnabled(false);
            txtNombreListaTareas.setEnabled(false);

        } else {
            lblmensaje.setForeground(Color.red);
            lblmensaje.setText("Algo Salio mal, favor asegurese que el archivo txt no este abierto");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

